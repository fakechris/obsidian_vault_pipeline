/// Parse `HH:MM` (24h). `9:30` is accepted; `24:00`, `09:60`, `0900` are not.
pub fn parse_time(s: &str) -> Result<(u8, u8), String> {
    let bad = || format!("invalid --time '{s}': expected HH:MM (24h), e.g. 09:00");
    let (h, m) = s.split_once(':').ok_or_else(bad)?;
    if h.is_empty() || h.len() > 2 || m.len() != 2 {
        return Err(bad());
    }
    let hour: u8 = h.parse().map_err(|_| bad())?;
    let minute: u8 = m.parse().map_err(|_| bad())?;
    if hour > 23 || minute > 59 {
        return Err(bad());
    }
    Ok((hour, minute))
}

/// Single-quote a string for /bin/sh.
pub fn sh_quote(s: &str) -> String {
    format!("'{}'", s.replace('\'', "'\\''"))
}

/// The command the OS entry runs on its interval: dispatch the registry. The
/// unit does NOT source the env file — each due job sources it itself (see
/// `scheduler::job_shell_command`), so credentials stay scoped to the jobs
/// that need them and the unit stays trivial.
pub fn tick_shell_command(cfg: &ScheduleConfig) -> String {
    format!(
        "exec {bin} schedule tick --vault-root {vault}",
        bin = sh_quote(&cfg.ovp2_path.display().to_string()),
        vault = sh_quote(&cfg.vault_root.display().to_string()),
    )
}

/// Inverse of [`xml_escape`] for metadata parsing (entities in the order
/// that can't double-decode: amp last).
fn xml_unescape(s: &str) -> String {
    s.replace("&lt;", "<")
        .replace("&gt;", ">")
        .replace("&quot;", "\"")
        .replace("&apos;", "'")
        .replace("&amp;", "&")
}

pub fn xml_escape(s: &str) -> String {
    s.replace('&', "&amp;")
        .replace('<', "&lt;")
        .replace('>', "&gt;")
}

/// `ovp2:` metadata lines `status` parses back out of a generated unit file.
/// The per-job schedule lives in the registry now, so the unit only records
/// where the vault + env file are (what `status` needs to read the registry).
fn metadata_pairs(cfg: &ScheduleConfig) -> Vec<(String, String)> {
    vec![
        ("vault-root".into(), cfg.vault_root.display().to_string()),
        ("env-file".into(), cfg.env_file.display().to_string()),
    ]
}

pub fn render_launchd_plist(cfg: &ScheduleConfig) -> String {
    let mut meta = String::new();
    for (k, v) in metadata_pairs(cfg) {
        meta.push_str(&format!("<!-- ovp2:{k}={} -->\n", xml_escape(&v)));
    }
    let log = xml_escape(&cfg.log_path(Flavor::Launchd).display().to_string());
    format!(
        r#"<?xml version="1.0" encoding="UTF-8"?>
<!DOCTYPE plist PUBLIC "-//Apple//DTD PLIST 1.0//EN" "http://www.apple.com/DTDs/PropertyList-1.0.dtd">
<!-- {header} -->
{meta}<plist version="1.0">
<dict>
    <key>Label</key>
    <string>{label}</string>
    <key>ProgramArguments</key>
    <array>
        <string>/bin/sh</string>
        <string>-c</string>
        <string>{command}</string>
    </array>
    <key>StartInterval</key>
    <integer>{interval}</integer>
    <key>StandardOutPath</key>
    <string>{log}</string>
    <key>StandardErrorPath</key>
    <string>{log}</string>
</dict>
</plist>
"#,
        header = GENERATED_HEADER,
        meta = meta,
        label = LAUNCHD_LABEL,
        command = xml_escape(&tick_shell_command(cfg)),
        interval = TICK_INTERVAL_SECS,
        log = log,
    )
}

/// Escape one argument for a systemd `ExecStart=` line, double-quoted style:
/// backslash-escape `\` and `"`, then neutralize systemd's own `$VAR`
/// expansion (`$` → `$$`) and `%` specifiers (`%` → `%%`).
pub fn systemd_quote_arg(s: &str) -> String {
    let escaped = s
        .replace('\\', "\\\\")
        .replace('"', "\\\"")
        .replace('$', "$$")
        .replace('%', "%%");
    format!("\"{escaped}\"")
}

fn systemd_metadata(cfg: &ScheduleConfig) -> String {
    let mut meta = format!("# {GENERATED_HEADER}\n");
    for (k, v) in metadata_pairs(cfg) {
        meta.push_str(&format!("# ovp2:{k}={v}\n"));
    }
    meta
}

pub fn render_systemd_service(cfg: &ScheduleConfig) -> String {
    format!(
        "{meta}\n[Unit]\nDescription=OVP2 scheduler tick\n\n[Service]\nType=oneshot\nExecStart=/bin/sh -c {cmd}\nStandardOutput=append:{log}\nStandardError=append:{log}\n",
        meta = systemd_metadata(cfg),
        cmd = systemd_quote_arg(&tick_shell_command(cfg)),
        // systemd expands %-specifiers in path directives — a vault path
        // like /home/me/100%notes must render as 100%%notes (codex P2).
        log = cfg
            .log_path(Flavor::Systemd)
            .display()
            .to_string()
            .replace('%', "%%"),
    )
}

pub fn render_systemd_timer(cfg: &ScheduleConfig) -> String {
    // Fire every TICK_INTERVAL_SECS; `tick` decides which jobs are actually
    // due. `*:0/10` = every 10 min. Persistent catches a missed slot after the
    // machine was asleep.
    let minutes = (TICK_INTERVAL_SECS / 60).max(1);
    format!(
        "{meta}\n[Unit]\nDescription=OVP2 scheduler tick timer\n\n[Timer]\nOnCalendar=*:0/{minutes}\nPersistent=true\n\n[Install]\nWantedBy=timers.target\n",
        meta = systemd_metadata(cfg),
    )
}

/// The env-file template `install` creates when `--env-file` does not exist.
/// This file IS meant to be hand-edited (unlike the unit files) — it holds
/// the credentials the scheduled run sources.
pub fn env_template() -> String {
    "\
# generated by ovp2 schedule install -- FILL IN and keep this file chmod 600.
# Sourced (`set -a; . <this file>`) before every scheduled `ovp2 daily` run,
# so plain KEY=value lines become environment variables. Never commit it.

# Required for --client live:
#ANTHROPIC_API_KEY=sk-ant-...

# Optional LLM overrides:
#ANTHROPIC_BASE_URL=
#OVP_LLM_MODEL=
#OVP_LLM_NO_PROXY=1

# Required for --github-live enrichment of GitHub repo URLs:
#GITHUB_TOKEN=

# The 180s default mis-kills slow provider responses on live runs; keep this.
OVP_LLM_TIMEOUT_SECS=480
"
    .to_string()
}

