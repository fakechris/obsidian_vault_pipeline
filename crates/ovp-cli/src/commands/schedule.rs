#[derive(Debug, Clone, PartialEq, Eq)]
pub struct UnitMeta {
    pub vault_root: PathBuf,
    pub env_file: PathBuf,
}

/// Recover the install-time config from a generated unit file's `ovp2:`
/// metadata comments (works for both the plist and the systemd units).
pub fn parse_unit_metadata(content: &str) -> Option<UnitMeta> {
    let mut vault_root = None;
    let mut env_file = None;
    for line in content.lines() {
        let line = line.trim();
        let Some(idx) = line.find("ovp2:") else {
            continue;
        };
        let rest = line[idx + "ovp2:".len()..].trim_end_matches("-->").trim();
        let Some((key, value)) = rest.split_once('=') else {
            continue;
        };
        // Launchd metadata comments store xml-escaped values — decode so
        // `status` resolves the real filesystem paths (codex review P2).
        let value = xml_unescape(value);
        let value = value.as_str();
        match key {
            "vault-root" => vault_root = Some(PathBuf::from(value)),
            "env-file" => env_file = Some(PathBuf::from(value)),
            _ => {}
        }
    }
    Some(UnitMeta {
        vault_root: vault_root?,
        env_file: env_file?,
    })
}

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct LaunchdJobState {
    pub loaded: bool,
    pub last_exit_status: Option<i64>,
}

/// Parse `launchctl list <label>` output. Exit failure = not loaded; on
/// success the plist-ish dump may carry `"LastExitStatus" = N;`.
pub fn parse_launchctl_list(success: bool, stdout: &str) -> LaunchdJobState {
    if !success {
        return LaunchdJobState {
            loaded: false,
            last_exit_status: None,
        };
    }
    let last_exit_status = stdout.lines().find_map(|line| {
        let line = line.trim();
        let rest = line.strip_prefix("\"LastExitStatus\"")?;
        let value = rest.trim_start_matches(|c: char| c == '=' || c.is_whitespace());
        value.trim_end_matches(';').trim().parse::<i64>().ok()
    });
    LaunchdJobState {
        loaded: true,
        last_exit_status,
    }
}

/// Latest run date in the daily ledger (ISO dates order lexicographically).
pub fn last_run_date(records: &[DailyRunRecord]) -> Option<String> {
    records.iter().map(|r| r.date.clone()).max()
}

/// Days-since-epoch for a civil date (Howard Hinnant's `days_from_civil`).
fn date_to_days(y: i64, m: i64, d: i64) -> i64 {
    let y = if m <= 2 { y - 1 } else { y };
    let era = if y >= 0 { y } else { y - 399 } / 400;
    let yoe = y - era * 400;
    let mp = (m + 9) % 12;
    let doy = (153 * mp + 2) / 5 + d - 1;
    let doe = yoe * 365 + yoe / 4 - yoe / 100 + doy;
    era * 146_097 + doe - 719_468
}

fn parse_iso_date(s: &str) -> Option<(i64, i64, i64)> {
    let mut it = s.splitn(3, '-');
    let y = it.next()?.parse().ok()?;
    let m = it.next()?.parse().ok()?;
    let d = it.next()?.parse().ok()?;
    if !(1..=12).contains(&m) || !(1..=31).contains(&d) {
        return None;
    }
    Some((y, m, d))
}

/// Signed whole days from `a` to `b` (both `YYYY-MM-DD`).
pub fn days_between(a: &str, b: &str) -> Option<i64> {
    let (ay, am, ad) = parse_iso_date(a)?;
    let (by, bm, bd) = parse_iso_date(b)?;
    Some(date_to_days(by, bm, bd) - date_to_days(ay, am, ad))
}

/// Staleness warning when the last run is more than STALE_AFTER_DAYS old.
pub fn staleness_warning(last_run: &str, today: &str) -> Option<String> {
    let days = days_between(last_run, today)?;
    if days > STALE_AFTER_DAYS {
        Some(format!(
            "last daily run was {days} days ago (> {STALE_AFTER_DAYS}) — the queue may be \
             stalling; check the log and run `ovp2 doctor`"
        ))
    } else {
        None
    }
}

fn tail_lines(content: &str, n: usize) -> Vec<&str> {
    let lines: Vec<&str> = content.lines().filter(|l| !l.trim().is_empty()).collect();
    let start = lines.len().saturating_sub(n);
    lines[start..].to_vec()
}

