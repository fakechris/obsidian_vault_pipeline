fn chmod_600(path: &Path) -> Result<(), String> {
    #[cfg(unix)]
    {
        use std::os::unix::fs::PermissionsExt;
        std::fs::set_permissions(path, std::fs::Permissions::from_mode(0o600))
            .map_err(|e| format!("chmod 600 {}: {e}", path.display()))?;
    }
    #[cfg(not(unix))]
    let _ = path;
    Ok(())
}

fn write_file_600(path: &Path, content: &str) -> Result<(), String> {
    if let Some(parent) = path.parent() {
        std::fs::create_dir_all(parent).map_err(|e| format!("mkdir {}: {e}", parent.display()))?;
    }
    std::fs::write(path, content).map_err(|e| format!("write {}: {e}", path.display()))?;
    chmod_600(path)
}

/// Create the env-file template if the file does not exist. Returns whether
/// it was created. An existing file is NEVER touched (it holds credentials).
pub fn ensure_env_file(path: &Path) -> Result<bool, String> {
    if path.exists() {
        return Ok(false);
    }
    write_file_600(path, &env_template())?;
    Ok(true)
}

#[derive(Debug)]
pub struct InstallReport {
    pub unit_files: Vec<PathBuf>,
    pub env_created: bool,
    /// What install did to the registry (seed vs reconcile env vs untouched).
    pub registry: RegistryOutcome,
}

/// Teardown of the pre-registry `com.ovp2.daily` unit so upgrading operators
/// don't run two schedulers — from BOTH install and uninstall, since an upgraded
/// vault may only have the old unit (codex P2). Returns the files it removed.
///
/// The unit file is removed ONLY after the OS confirms the job is no longer
/// loaded/active: if the stop command fails AND the job is still running, we
/// abort rather than orphan a live job whose file we deleted (codex P1) — a
/// failed stop for an already-stopped job is benign.
fn remove_legacy_unit(
    flavor: Flavor,
    home: &Path,
    runner: &dyn CommandRunner,
) -> Result<Vec<PathBuf>, CliError> {
    let mut removed = Vec::new();
    match flavor {
        Flavor::Launchd => {
            let plist = home
                .join("Library/LaunchAgents")
                .join(format!("{LEGACY_LAUNCHD_LABEL}.plist"));
            if plist.exists() {
                let out = runner
                    .run("launchctl", &["unload", &plist.display().to_string()])
                    .map_err(CliError::Io)?;
                if !out.success {
                    // Distinguish a real stop failure from "wasn't loaded".
                    let list = runner
                        .run("launchctl", &["list", LEGACY_LAUNCHD_LABEL])
                        .map_err(CliError::Io)?;
                    if parse_launchctl_list(list.success, &list.stdout).loaded {
                        return Err(CliError::Io(format!(
                            "legacy job {LEGACY_LAUNCHD_LABEL} is still loaded and could not be \
                             unloaded ({}); stop it by hand before installing the scheduler",
                            out.stderr.trim()
                        )));
                    }
                }
                // A leftover plist auto-loads at next login alongside the new
                // scheduler → duplicate runs, so a failed delete must fail loud.
                std::fs::remove_file(&plist)
                    .map_err(|e| CliError::Io(format!("remove legacy {}: {e}", plist.display())))?;
                removed.push(plist);
            }
        }
        Flavor::Systemd => {
            let dir = home.join(".config/systemd/user");
            let timer = dir.join(format!("{LEGACY_SYSTEMD_UNIT}.timer"));
            let service = dir.join(format!("{LEGACY_SYSTEMD_UNIT}.service"));
            if timer.exists() || service.exists() {
                let timer_unit = format!("{LEGACY_SYSTEMD_UNIT}.timer");
                let out = runner
                    .run("systemctl", &["--user", "disable", "--now", &timer_unit])
                    .map_err(CliError::Io)?;
                if !out.success {
                    let active = runner
                        .run("systemctl", &["--user", "is-active", &timer_unit])
                        .map_err(CliError::Io)?;
                    if first_word_or(&active.stdout, "") == "active" {
                        return Err(CliError::Io(format!(
                            "legacy timer {timer_unit} is still active and could not be disabled \
                             ({}); stop it by hand before installing the scheduler",
                            out.stderr.trim()
                        )));
                    }
                }
                for p in [timer, service] {
                    if p.exists() {
                        std::fs::remove_file(&p).map_err(|e| {
                            CliError::Io(format!("remove legacy {}: {e}", p.display()))
                        })?;
                        removed.push(p);
                    }
                }
            }
        }
    }
    Ok(removed)
}

/// What `ensure_registry` did to `.ovp/schedule.json`.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum RegistryOutcome {
    /// Written fresh with the default jobs.
    Seeded,
    /// Existed; only the install-owned `env_file` was reconciled (jobs kept).
    EnvUpdated,
    /// Existed and already matched — left untouched.
    Unchanged,
}

/// The configured env file as a registry value: `{vault}`-relative when it
/// lives under the vault (portable), absolute otherwise.
fn registry_env_value(cfg: &ScheduleConfig) -> String {
    match cfg.env_file.strip_prefix(&cfg.vault_root) {
        Ok(rel) => format!("{}/{}", super::scheduler::VAULT_PLACEHOLDER, rel.display()),
        Err(_) => cfg.env_file.display().to_string(),
    }
}

/// Ensure `.ovp/schedule.json` reflects this install. Seeds the default jobs if
/// absent; otherwise preserves operator job edits but reconciles the
/// install-owned `env_file` (a re-install with a new `--env-file` must not
/// leave the dispatcher sourcing the old credentials — codex P1).
fn ensure_registry(cfg: &ScheduleConfig) -> Result<RegistryOutcome, CliError> {
    let env_value = registry_env_value(cfg);
    if !super::scheduler::registry_path(&cfg.vault_root).exists() {
        let client = match cfg.client {
            ScheduleClient::Live => "live",
            ScheduleClient::Replay => "replay",
        };
        let mut reg = super::scheduler::default_registry(
            client,
            (cfg.hour, cfg.minute),
            cfg.enrich,
            cfg.max_sources,
        );
        reg.env_file = Some(env_value);
        super::scheduler::save_registry(&cfg.vault_root, &reg)?;
        return Ok(RegistryOutcome::Seeded);
    }
    // Existing registry: keep jobs, reconcile the install-owned env file.
    let mut reg = super::scheduler::load_registry(&cfg.vault_root)?
        .expect("registry_path exists but load returned None");
    if reg.env_file.as_deref() == Some(env_value.as_str()) {
        return Ok(RegistryOutcome::Unchanged);
    }
    reg.env_file = Some(env_value);
    super::scheduler::save_registry(&cfg.vault_root, &reg)?;
    Ok(RegistryOutcome::EnvUpdated)
}

pub fn install_with(
    cfg: &ScheduleConfig,
    flavor: Flavor,
    home: &Path,
    runner: &dyn CommandRunner,
) -> Result<InstallReport, CliError> {
    let env_created = ensure_env_file(&cfg.env_file).map_err(CliError::Io)?;
    // Hold the dispatch lock across the registry read-modify-write + state
    // seeding so a concurrent enable/disable/desktop edit can't be lost or
    // race the shared temp file (codex P2).
    let registry = {
        let _lock = super::scheduler::acquire_dispatch_lock(&cfg.vault_root)?;
        let registry = ensure_registry(cfg)?;
        // Freshly installed jobs run at their NEXT occurrence, not on the first
        // tick (codex P1) — seed state only when none exists yet.
        super::scheduler::seed_state_if_absent(&cfg.vault_root)?;
        registry
    };
    let logs_dir = cfg.vault_root.join(".ovp/logs");
    std::fs::create_dir_all(&logs_dir)
        .map_err(|e| CliError::Io(format!("mkdir {}: {e}", logs_dir.display())))?;

    let paths = unit_paths(home, flavor);
    match flavor {
        Flavor::Launchd => {
            let plist = &paths[0];
            let plist_str = plist.display().to_string();
            // Idempotency: unload whatever is loaded from this path (a
            // previous install, or the interim hand-written plist with the
            // same label). Failure just means nothing was loaded.
            let _ = runner.run("launchctl", &["unload", &plist_str]);
            write_file_600(plist, &render_launchd_plist(cfg)).map_err(CliError::Io)?;
            let out = runner
                .run("launchctl", &["load", &plist_str])
                .map_err(CliError::Io)?;
            if !out.success {
                return Err(CliError::Io(format!(
                    "launchctl load {} failed: {}",
                    plist_str,
                    out.stderr.trim()
                )));
            }
        }
        Flavor::Systemd => {
            write_file_600(&paths[0], &render_systemd_service(cfg)).map_err(CliError::Io)?;
            write_file_600(&paths[1], &render_systemd_timer(cfg)).map_err(CliError::Io)?;
            let timer = format!("{SYSTEMD_UNIT}.timer");
            for args in [
                vec!["--user", "daemon-reload"],
                vec!["--user", "enable", "--now", timer.as_str()],
            ] {
                let out = runner.run("systemctl", &args).map_err(CliError::Io)?;
                if !out.success {
                    return Err(CliError::Io(format!(
                        "systemctl {} failed: {}",
                        args.join(" "),
                        out.stderr.trim()
                    )));
                }
            }
        }
    }
    // Retire the pre-registry unit ONLY now that the new scheduler is written
    // and loaded — an earlier failure (bad registry, unwritable state, failed
    // load) must not leave the operator with no scheduler at all (codex P2).
    remove_legacy_unit(flavor, home, runner)?;
    Ok(InstallReport {
        unit_files: paths,
        env_created,
        registry,
    })
}

/// Unload/disable + remove the unit files. Logs and the env file are left
/// untouched. Returns the files actually removed (empty = nothing installed).
pub fn uninstall_with(
    flavor: Flavor,
    home: &Path,
    runner: &dyn CommandRunner,
) -> Result<Vec<PathBuf>, CliError> {
    // Tear down the pre-registry unit too — an operator who upgraded from main
    // and never re-installed only has `com.ovp2.daily`, and uninstall must not
    // leave it running (codex P2).
    let mut removed = remove_legacy_unit(flavor, home, runner)?;
    let paths = unit_paths(home, flavor);
    let installed: Vec<&PathBuf> = paths.iter().filter(|p| p.exists()).collect();
    if installed.is_empty() {
        return Ok(removed);
    }
    match flavor {
        Flavor::Launchd => {
            // Best-effort unload; the file may exist without being loaded.
            let _ = runner.run("launchctl", &["unload", &paths[0].display().to_string()]);
        }
        Flavor::Systemd => {
            let timer = format!("{SYSTEMD_UNIT}.timer");
            let _ = runner.run("systemctl", &["--user", "disable", "--now", &timer]);
        }
    }
    for p in installed {
        std::fs::remove_file(p)
            .map_err(|e| CliError::Io(format!("remove {}: {e}", p.display())))?;
        removed.push(p.clone());
    }
    if flavor == Flavor::Systemd {
        let _ = runner.run("systemctl", &["--user", "daemon-reload"]);
    }
    Ok(removed)
}

