pub struct RunOutput {
    pub success: bool,
    pub stdout: String,
    pub stderr: String,
}

pub trait CommandRunner {
    fn run(&self, program: &str, args: &[&str]) -> Result<RunOutput, String>;
}

pub struct SystemRunner;

impl CommandRunner for SystemRunner {
    fn run(&self, program: &str, args: &[&str]) -> Result<RunOutput, String> {
        let out = std::process::Command::new(program)
            .args(args)
            .output()
            .map_err(|e| format!("failed to run {program}: {e}"))?;
        Ok(RunOutput {
            success: out.status.success(),
            stdout: String::from_utf8_lossy(&out.stdout).into_owned(),
            stderr: String::from_utf8_lossy(&out.stderr).into_owned(),
        })
    }
}

fn detect_flavor() -> Result<Flavor, CliError> {
    if cfg!(target_os = "macos") {
        Ok(Flavor::Launchd)
    } else if cfg!(target_os = "linux") {
        Ok(Flavor::Systemd)
    } else {
        Err(CliError::Io(
            "ovp2 schedule is not supported on this platform — only macOS (launchd) \
             and Linux (systemd user timers). Run `ovp2 daily` from your own scheduler."
                .into(),
        ))
    }
}

fn home_dir() -> Result<PathBuf, CliError> {
    std::env::var_os("HOME")
        .map(PathBuf::from)
        .filter(|p| !p.as_os_str().is_empty())
        .ok_or_else(|| {
            CliError::Io("HOME is not set — cannot locate the user unit directory".into())
        })
}

/// The unit file(s) `install` writes for a flavor, under the given home dir.
/// First entry is the one `status` parses (the plist / the .service).
pub fn unit_paths(home: &Path, flavor: Flavor) -> Vec<PathBuf> {
    match flavor {
        Flavor::Launchd => vec![
            home.join("Library/LaunchAgents")
                .join(format!("{LAUNCHD_LABEL}.plist")),
        ],
        Flavor::Systemd => {
            let dir = home.join(".config/systemd/user");
            vec![
                dir.join(format!("{SYSTEMD_UNIT}.service")),
                dir.join(format!("{SYSTEMD_UNIT}.timer")),
            ]
        }
    }
}

