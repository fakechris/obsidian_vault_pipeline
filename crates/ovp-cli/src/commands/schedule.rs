fn absolutize(p: PathBuf) -> Result<PathBuf, CliError> {
    if p.is_absolute() {
        Ok(p)
    } else {
        std::env::current_dir()
            .map(|cwd| cwd.join(p))
            .map_err(|e| CliError::Io(format!("cannot resolve current dir: {e}")))
    }
}

pub fn run_install(args: InstallArgs) -> Result<(), CliError> {
    let flavor = detect_flavor()?;
    let home = home_dir()?;
    let (hour, minute) = parse_time(&args.time).map_err(CliError::Io)?;
    let vault_root = args.vault_root.canonicalize().map_err(|e| {
        CliError::Io(format!(
            "vault root {} is not usable: {e}",
            args.vault_root.display()
        ))
    })?;
    let env_file = match args.env_file {
        Some(p) => absolutize(p)?,
        None => vault_root.join(".ovp/daily.env"),
    };
    let ovp2_path = std::env::current_exe()
        .map_err(|e| CliError::Io(format!("cannot resolve the ovp2 binary path: {e}")))?;
    let cfg = ScheduleConfig {
        vault_root,
        hour,
        minute,
        max_sources: args.max_sources,
        client: args.client,
        env_file,
        enrich: args.enrich,
        ovp2_path,
    };

    let report = install_with(&cfg, flavor, &home, &SystemRunner)?;

    println!("schedule installed ({})", flavor_name(flavor));
    for p in &report.unit_files {
        println!("  unit:      {}", p.display());
    }
    println!(
        "  runs:      scheduler tick every {} min (dispatches due jobs)",
        TICK_INTERVAL_SECS / 60
    );
    let reg_path = super::scheduler::registry_path(&cfg.vault_root).display().to_string();
    match report.registry {
        RegistryOutcome::Seeded => {
            println!("  registry:  {reg_path} (NEW — daily {} + weekly crystallize)", cfg.time_string());
            println!("             edit it or use `ovp2 schedule enable/disable <id>`");
        }
        RegistryOutcome::EnvUpdated => {
            println!("  registry:  {reg_path} (existing jobs kept; env-file updated)");
        }
        RegistryOutcome::Unchanged => {
            println!("  registry:  {reg_path} (existing, left untouched)");
        }
    }
    println!("  vault:     {}", cfg.vault_root.display());
    println!("  binary:    {}", cfg.ovp2_path.display());
    println!("  log:       {}", cfg.log_path(flavor).display());
    if report.env_created {
        println!(
            "  env-file:  {} (NEW template, chmod 600)",
            cfg.env_file.display()
        );
        println!(
            "             fill in ANTHROPIC_API_KEY (required for --client live); optional:\n\
             \x20            ANTHROPIC_BASE_URL, OVP_LLM_MODEL, OVP_LLM_NO_PROXY, GITHUB_TOKEN"
        );
    } else {
        println!(
            "  env-file:  {} (existing, left untouched)",
            cfg.env_file.display()
        );
    }
    println!(
        "  note:      the job pins this binary path — re-run `ovp2 schedule install`\n\
         \x20            after moving the binary (brew upgrades keep the path stable)."
    );
    println!("  check:     ovp2 schedule status  ·  jobs: ovp2 schedule list --vault-root <v>");
    Ok(())
}

pub fn run_uninstall() -> Result<(), CliError> {
    let flavor = detect_flavor()?;
    let home = home_dir()?;
    let removed = uninstall_with(flavor, &home, &SystemRunner)?;
    if removed.is_empty() {
        println!(
            "schedule: nothing installed ({}) — nothing to remove",
            flavor_name(flavor)
        );
    } else {
        println!("schedule uninstalled ({})", flavor_name(flavor));
        for p in removed {
            println!("  removed:   {}", p.display());
        }
        println!("  kept:      logs and the env file (remove by hand if unwanted)");
    }
    Ok(())
}

pub fn run_status(today: &str) -> Result<(), CliError> {
    let flavor = detect_flavor()?;
    let home = home_dir()?;
    status_with(flavor, &home, &SystemRunner, today)
}

fn flavor_name(flavor: Flavor) -> &'static str {
    match flavor {
        Flavor::Launchd => "launchd",
        Flavor::Systemd => "systemd user timer",
    }
}

fn status_with(
    flavor: Flavor,
    home: &Path,
    runner: &dyn CommandRunner,
    today: &str,
) -> Result<(), CliError> {
    let paths = unit_paths(home, flavor);
    let unit = &paths[0];
    println!("schedule status ({})", flavor_name(flavor));
    if !unit.exists() {
        println!("  not installed — run: ovp2 schedule install --vault-root <vault>");
        return Ok(());
    }
    println!("  unit:      {}", unit.display());

    // Loaded / enabled state from the OS scheduler.
    match flavor {
        Flavor::Launchd => {
            let out = runner
                .run("launchctl", &["list", LAUNCHD_LABEL])
                .map_err(CliError::Io)?;
            let state = parse_launchctl_list(out.success, &out.stdout);
            if state.loaded {
                match state.last_exit_status {
                    Some(code) => println!("  loaded:    yes (last exit status {code})"),
                    None => println!("  loaded:    yes"),
                }
            } else {
                println!("  loaded:    NO — re-run `ovp2 schedule install`");
            }
        }
        Flavor::Systemd => {
            let timer = format!("{SYSTEMD_UNIT}.timer");
            let enabled = runner
                .run("systemctl", &["--user", "is-enabled", &timer])
                .map_err(CliError::Io)?;
            let active = runner
                .run("systemctl", &["--user", "is-active", &timer])
                .map_err(CliError::Io)?;
            println!(
                "  timer:     {} / {}",
                first_word_or(&enabled.stdout, "unknown"),
                first_word_or(&active.stdout, "unknown")
            );
            if !enabled.success || !active.success {
                println!("             not fully enabled — re-run `ovp2 schedule install`");
            }
        }
    }

    // Install-time config, recovered from the unit file's metadata comments.
    let content = std::fs::read_to_string(unit)
        .map_err(|e| CliError::Io(format!("read {}: {e}", unit.display())))?;
    let Some(meta) = parse_unit_metadata(&content) else {
        println!(
            "  WARN: unit file has no ovp2 metadata (hand-edited?) — re-run `ovp2 schedule install`"
        );
        return Ok(());
    };
    println!(
        "  tick:      every {} min (jobs live in the registry)",
        TICK_INTERVAL_SECS / 60
    );
    println!("  vault:     {}", meta.vault_root.display());
    let env_state = if meta.env_file.exists() {
        "present"
    } else {
        "MISSING"
    };
    println!("  env-file:  {} ({env_state})", meta.env_file.display());
    // The per-job schedule (cadences, last run, next due, "due now") lives in
    // the registry — render it here so `status` reflects failed/overdue jobs a
    // no-op tick's exit code would otherwise have masked (codex P2).
    let reg_path = super::scheduler::registry_path(&meta.vault_root);
    println!("  registry:  {}", reg_path.display());
    match super::scheduler::load_registry(&meta.vault_root) {
        Ok(Some(reg)) => match super::scheduler::load_state(&meta.vault_root) {
            Ok(state) => {
                super::scheduler::print_jobs(&reg, &state, super::scheduler::local_now(), "  ")
            }
            // A malformed state file blocks `tick`, so surface it rather than
            // silently showing every job as never-run/due-now (codex P2).
            Err(e) => println!("             WARN: cannot read schedule state: {e}"),
        },
        Ok(None) => println!("             MISSING — re-run `ovp2 schedule install`"),
        Err(e) => println!("             WARN: cannot read registry: {e}"),
    }

    // Last log lines.
    let log = meta
        .vault_root
        .join(".ovp/logs")
        .join(log_file_name(flavor));
    match std::fs::read_to_string(&log) {
        Ok(text) => {
            println!("  log:       {}", log.display());
            for line in tail_lines(&text, 5) {
                println!("    | {line}");
            }
        }
        Err(_) => println!("  log:       {} (no log yet)", log.display()),
    }

    // Last run — PREFER the run-liveness heartbeat (`.ovp/last-run.json`, OVP2
    // observability P0): it carries the terminal status of the most recent run,
    // so an unattended run that FAILED or ABORTED before writing a report is
    // still surfaced here. The append-only ledger's date is the fallback for a
    // vault whose last successful run predates the heartbeat.
    match ovp_daily::read_last_run(&meta.vault_root) {
        Ok(Some(hb)) => {
            let when = hb.ended_at.as_deref().unwrap_or(hb.started_at.as_str());
            let status = match hb.status {
                ovp_daily::LastRunStatus::Completed => "completed",
                ovp_daily::LastRunStatus::Failed => "FAILED",
                ovp_daily::LastRunStatus::Aborted => "ABORTED",
                ovp_daily::LastRunStatus::Running => "running",
            };
            println!("  last run:  {status} at {when} ({})", hb.run_id);
            match hb.status {
                ovp_daily::LastRunStatus::Failed | ovp_daily::LastRunStatus::Aborted => {
                    println!(
                        "  WARN: the last run did not complete ({status}){} — check the schedule log and rerun `ovp2 daily`",
                        hb.error.as_deref().map(|e| format!(": {e}")).unwrap_or_default()
                    );
                }
                _ => {}
            }
        }
        Ok(None) => report_last_run_from_ledger(&meta.vault_root, today),
        Err(e) => {
            println!("  WARN: cannot read .ovp/last-run.json: {e}");
            report_last_run_from_ledger(&meta.vault_root, today);
        }
    }
    Ok(())
}

/// Fallback last-run report from the append-only daily ledger (date only; no
/// terminal status), used when the heartbeat file is absent or unreadable.
fn report_last_run_from_ledger(vault_root: &Path, today: &str) {
    let ledger_path = vault_root.join(".ovp/daily-runs.jsonl");
    match read_daily_ledger(&ledger_path) {
        Ok(records) => match last_run_date(&records) {
            Some(last) => {
                let ago = days_between(&last, today)
                    .map(|d| format!(" ({d} day(s) ago)"))
                    .unwrap_or_default();
                println!("  last run:  {last}{ago}");
                if let Some(warn) = staleness_warning(&last, today) {
                    println!("  WARN: {warn}");
                }
            }
            None => println!("  last run:  never (empty ledger)"),
        },
        Err(e) => println!("  WARN: cannot read {}: {e}", ledger_path.display()),
    }
}

fn first_word_or<'a>(s: &'a str, fallback: &'a str) -> &'a str {
    let w = s.trim();
    if w.is_empty() {
        fallback
    } else {
        w.lines().next().unwrap_or(fallback)
    }
}

