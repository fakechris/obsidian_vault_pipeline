//! `schedule` — PRODUCT: install a SINGLE OS entry that dispatches the job
//! registry, so the pipeline heartbeat no longer depends on a human remembering
//! to run `ovp2 daily` (M32 §9 decided OS scheduler over daemon).
//!
//! - macOS: a launchd user agent (`~/Library/LaunchAgents/com.ovp2.scheduler.plist`).
//! - Linux: a systemd user service + timer (`~/.config/systemd/user/ovp2-scheduler.*`).
//! - Anything else: a clear "not supported" error.
//!
//! The OS entry runs `ovp2 schedule tick --vault-root <v>` every
//! [`TICK_INTERVAL_SECS`]; `tick` reads `.ovp/schedule.json` (the registry) and
//! runs the jobs that are due (see [`super::scheduler`]). Each due job sources
//! the env file itself, so the unit stays trivial and credentials scope to the
//! jobs that need them. `install` seeds the registry with the default jobs
//! (daily + weekly crystallize) unless one already exists (operator edits are
//! preserved), migrates away the pre-registry `com.ovp2.daily` unit, and is
//! idempotent (unload first, overwrite, reload). `uninstall` removes the unit
//! files but leaves logs, the env file, and the registry untouched. The unit
//! files carry machine-readable `ovp2:` metadata comments that `status` parses
//! back to locate the vault + env file.

use std::path::{Path, PathBuf};

use ovp_daily::{DailyRunRecord, read_daily_ledger};

use crate::CliError;

/// launchd job label (also the plist file stem). A hand-written interim plist
/// with the same label is superseded cleanly by `install` (unload + overwrite).
pub const LAUNCHD_LABEL: &str = "com.ovp2.scheduler";
/// systemd unit stem: `ovp2-scheduler.service` + `ovp2-scheduler.timer`.
pub const SYSTEMD_UNIT: &str = "ovp2-scheduler";

/// The pre-registry label this scheduler superseded. `install` unloads/removes
/// it so an operator upgrading from the single hardwired `daily` unit does not
/// end up running two schedulers.
pub const LEGACY_LAUNCHD_LABEL: &str = "com.ovp2.daily";
pub const LEGACY_SYSTEMD_UNIT: &str = "ovp2-daily";

/// How often the single OS entry runs `schedule tick`. The tick itself decides
/// which jobs are due, so this only bounds scheduling granularity (a 09:00 job
/// fires within 10 min of 09:00).
pub const TICK_INTERVAL_SECS: u32 = 600;

const GENERATED_HEADER: &str =
    "generated by ovp2 schedule -- do not hand-edit; re-run `ovp2 schedule install`";

/// How many days without a daily run before `status` warns.
const STALE_AFTER_DAYS: i64 = 2;

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum ScheduleClient {
    Live,
    Replay,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Flavor {
    Launchd,
    Systemd,
}

pub struct InstallArgs {
    pub vault_root: PathBuf,
    /// Local time of day, `HH:MM` (24h).
    pub time: String,
    pub max_sources: Option<usize>,
    pub client: ScheduleClient,
    /// Env file sourced before each run. Default: `<vault>/.ovp/daily.env`.
    pub env_file: Option<PathBuf>,
    /// Pass live enrichment flags (`--web-fetch-live --github-live`).
    pub enrich: bool,
}

/// Everything the generators need, fully resolved (absolute paths, parsed time).
#[derive(Debug, Clone)]
pub struct ScheduleConfig {
    pub vault_root: PathBuf,
    pub hour: u8,
    pub minute: u8,
    pub max_sources: Option<usize>,
    pub client: ScheduleClient,
    pub env_file: PathBuf,
    pub enrich: bool,
    /// The binary the job executes — `std::env::current_exe()` at install
    /// time. Moving the binary requires re-running `install`.
    pub ovp2_path: PathBuf,
}

impl ScheduleConfig {
    pub fn time_string(&self) -> String {
        format!("{:02}:{:02}", self.hour, self.minute)
    }

    pub fn log_path(&self, flavor: Flavor) -> PathBuf {
        self.vault_root
            .join(".ovp/logs")
            .join(log_file_name(flavor))
    }
}

fn log_file_name(flavor: Flavor) -> &'static str {
    match flavor {
        Flavor::Launchd => "scheduler-launchd.log",
        Flavor::Systemd => "scheduler-systemd.log",
    }
}

