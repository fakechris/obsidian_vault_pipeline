def test_dispatch_unknown_prefix_raises_no_handler(tmp_path: Path):
    vault = _make_vault(tmp_path)
    task = _write_task(vault, "FOO-bar.md")
    with pytest.raises(NoHandlerError):
        dispatch_task(vault, task, llm_client=_FakeLLM())
