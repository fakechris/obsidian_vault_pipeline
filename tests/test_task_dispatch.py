def test_cli_list_handlers(capsys):
    rc = main(["--vault-dir", "/tmp", "--list-handlers"])
    assert rc == 0
    out = capsys.readouterr().out
    for prefix in ("RESEARCH", "SYNTHESIZE", "CONTRADICT"):
        assert prefix in out


