def test_dispatch_handler_exception_audited(tmp_path: Path):
    vault = _make_vault(tmp_path)

    def _boom(ctx: TaskContext) -> TaskResult:
        raise RuntimeError("handler exploded")

    register_handler("BOOM", _boom, "test only")
    task = _write_task(vault, "BOOM-stuff.md")
    with pytest.raises(DispatchError):
        dispatch_task(vault, task, llm_client=_FakeLLM())

    # task_failed event recorded.
    pipeline_log = (vault / "60-Logs" / "pipeline.jsonl").read_text(encoding="utf-8")
    assert "task_failed" in pipeline_log
    # Original task NOT archived (handler failed before archival).
    assert task.exists()


# ── rate limiting ───────────────────────────────────────────────


def test_rate_limit_blocks_after_cap(tmp_path: Path):
    vault = _make_vault(tmp_path)
    llm = _FakeLLM()

    # Cap at 2 — dispatch two, third must raise.
    t1 = _write_task(vault, "RESEARCH-one.md")
    t2 = _write_task(vault, "RESEARCH-two.md")
    t3 = _write_task(vault, "RESEARCH-three.md")
    dispatch_task(vault, t1, llm_client=llm, rate_limit=2)
    dispatch_task(vault, t2, llm_client=llm, rate_limit=2)
    with pytest.raises(RateLimitedError):
        dispatch_task(vault, t3, llm_client=llm, rate_limit=2)

    # Third file untouched in the queue.
    assert t3.exists()
    # task_rate_limited event recorded.
    pipeline_log = (vault / "60-Logs" / "pipeline.jsonl").read_text(encoding="utf-8")
    assert "task_rate_limited" in pipeline_log


# ── list_pending_tasks ──────────────────────────────────────────


def test_list_pending_skips_non_matching_files(tmp_path: Path):
    vault = _make_vault(tmp_path)
    _write_task(vault, "RESEARCH-a.md")
    _write_task(vault, "SYNTHESIZE-b.md")
    _write_task(vault, "README.md")              # ignored
    _write_task(vault, "notes-on-stuff.md")      # ignored

    pending = list_pending_tasks(vault)
    names = sorted(p.name for p in pending)
    assert names == ["RESEARCH-a.md", "SYNTHESIZE-b.md"]


